class DataTypes {
    public static void main(String[] args) {

        // Boolean
        boolean a = true;
        boolean b = false;
        System.out.println("This are the boolean data types " + a + " " + b);

        // int, float, char, double, long
        int c = 10;
        float d = 10.0f;
        long e = 10000000000l;
        double f = 154655435434d;
        String name = "SmartMonk-E is the best";
        char g = 'g';

        System.out.println(c + "\t" + d + "\t" + e);
        System.out.println(e + "\t" + name + "\t" + g);

    }
}